package TasksLinearPrograms;

public class Tasks01 {
    public static void main(String[] args) {
        int x;
        int y;

        x = 6;
        y = 2;

        int sum;
        int dif;
        int prod;
        int quot;

        sum = x + y;
        dif = x - y;
        prod = x * y;
        quot = x / y;

        System.out.println("сумма чисел = " + sum);
        System.out.println("разность чисел = " + dif);
        System.out.println("произведение чисел = " + prod);
        System.out.println("частное чисел = " + quot);
    }
}
