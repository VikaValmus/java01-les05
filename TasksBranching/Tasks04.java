package TasksBranching;

public class Tasks04 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        if (a == b) {
            System.out.println(a + " равно " + b);
        } else if (a > b) {
            System.out.println(a + " больше " + b);
        } else {
            System.out.println(a + " меньше " + b);
        }
    }
}
